function describeRestrictionDuration(mode: RestrictionDurationMode, amount: number) {
  if (mode === "manual") return "until manual restore";
  const unit = amount === 1 ? { minutes: "minute", hours: "hour", days: "day" }[mode] : mode;
  return `for ${amount} ${unit}`;
}

function chatResetScopeCount(report: ChatReconciliationReport, scope: ChatResetScope) {
  if (scope === "conversations") return report.directConversations;
  if (scope === "personnel_groups") return report.personnelGroups;
  if (scope === "system_groups") return report.instituteGroups + report.divisionGroups;
  return report.teamGroups;
}

